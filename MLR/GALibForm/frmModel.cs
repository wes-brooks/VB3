        private double getTransformPower(string pwrTransform)
        {
            if (String.IsNullOrWhiteSpace(pwrTransform))
                return double.NaN;

            char[] delim = ",".ToCharArray();
            string[] svals = pwrTransform.Split(delim);

            double power = 1.0;
            if (svals.Length != 2)
                return double.NaN;

            if (!Double.TryParse(svals[1], out power))
                return double.NaN;

            return power;
        }
        

